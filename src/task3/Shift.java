public class Shift {
    public static void main(String[] args) {
        int a = 10;
        int b = 8;
        int c = 499;
        a = a << 1;
        b = b >> 1;
        c = c >>> 1;
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);

    }
}
